#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*a;

	i = 1;
	while (i < argc)
	{
		a = argv[i];
		while (*a)
		{
			write(1, a, 1);
			a++;
		}
		write(1, "\n", 1);
		i++;
	}
}
