#include <unistd.h>

void	ft_print_comb(void)
{
	char	res[3];
	int		num[3];

	num[0] = 0;
	while (num[0] <= 7)
	{
		res[0] = num[0] + 48;
		num[1] = num[0] + 1;
		while (num[1] <= 8)
		{
			res[1] = num[1] + 48;
			num[2] = num[1] + 1;
			while (num[2] <= 9)
			{
				res[2] = num[2] + 48;
				write(1, res, 3);
				if (!((num[0] == 7) && (num[1] == 8) && (num[2] == 9)))
					write(1, ", ", 2);
				++num[2];
			}
			++num[1];
		}
		++num[0];
	}
}
